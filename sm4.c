// 循环左移
static inline uint32_t rotate_left(uint32_t x, int n) {
    return (x << n) | (x >> (32 - n));
}

// 密钥扩展中的T'变换
static uint32_t sm4_t_prime(uint32_t x) {
    uint8_t b[4];
    b[0] = (x >> 24) & 0xFF;
    b[1] = (x >> 16) & 0xFF;
    b[2] = (x >> 8) & 0xFF;
    b[3] = x & 0xFF;
    for (int i = 0; i < 4; i++) {
        b[i] = SM4_SBOX[b[i]];
    }
    uint32_t y = (b[0] << 24) | (b[1] << 16) | (b[2] << 8) | b[3];
    return y ^ rotate_left(y, 13) ^ rotate_left(y, 23);
}

// 加密中的T变换
static uint32_t sm4_t(uint32_t x) {
    uint8_t b[4];
    b[0] = (x >> 24) & 0xFF;
    b[1] = (x >> 16) & 0xFF;
    b[2] = (x >> 8) & 0xFF;
    b[3] = x & 0xFF;
    for (int i = 0; i < 4; i++) {
        b[i] = SM4_SBOX[b[i]];
    }
    uint32_t y = (b[0] << 24) | (b[1] << 16) | (b[2] << 8) | b[3];
    return y ^ rotate_left(y, 2) ^ rotate_left(y, 10) ^ rotate_left(y, 18) ^ rotate_left(y, 24);
}

// 字节数组转换为32位字（大端）
static void bytes_to_words(const uint8_t *bytes, uint32_t *words) {
    for (int i = 0; i < 4; i++) {
        words[i] = ((uint32_t)bytes[4*i] << 24) |
                   ((uint32_t)bytes[4*i+1] << 16) |
                   ((uint32_t)bytes[4*i+2] << 8) |
                   (uint32_t)bytes[4*i+3];
    }
}

// 32位字转换为字节数组（大端）
static void words_to_bytes(const uint32_t *words, uint8_t *bytes) {
    for (int i = 0; i < 4; i++) {
        bytes[4*i]   = (words[i] >> 24) & 0xFF;
        bytes[4*i+1] = (words[i] >> 16) & 0xFF;
        bytes[4*i+2] = (words[i] >> 8) & 0xFF;
        bytes[4*i+3] = words[i] & 0xFF;
    }
}

// 密钥扩展
void sm4_key_schedule(const uint8_t key[16], uint32_t rk[32]) {
    uint32_t MK[4];
    bytes_to_words(key, MK);
    uint32_t K[36];
    K[0] = MK[0] ^ FK[0];
    K[1] = MK[1] ^ FK[1];
    K[2] = MK[2] ^ FK[2];
    K[3] = MK[3] ^ FK[3];
    for (int i = 0; i < 32; i++) {
        uint32_t tmp = K[i+1] ^ K[i+2] ^ K[i+3] ^ CK[i];
        tmp = sm4_t_prime(tmp);
        K[i+4] = K[i] ^ tmp;
        rk[i] = K[i+4];
    }
}

// SM4加密单个块
void sm4_encrypt_block(const uint32_t rk[32], const uint8_t plaintext[16], uint8_t ciphertext[16]) {
    uint32_t X[4];
    bytes_to_words(plaintext, X);
    for (int i = 0; i < 32; i++) {
        uint32_t tmp = X[1] ^ X[2] ^ X[3] ^ rk[i];
        tmp = sm4_t(tmp);
        uint32_t X4 = X[0] ^ tmp;
        X[0] = X[1];
        X[1] = X[2];
        X[2] = X[3];
        X[3] = X4;
    }
    uint32_t Y[4] = {X[3], X[2], X[1], X[0]};
    words_to_bytes(Y, ciphertext);
}

// SM4-CBC加密
void sm4_cbc_encrypt(const uint8_t *input, uint8_t *output, size_t length,
                     const uint8_t key[16], const uint8_t iv[16]) {
    uint32_t rk[32];
    sm4_key_schedule(key, rk);
    uint8_t current_iv[16];
    memcpy(current_iv, iv, 16);
    for (size_t i = 0; i < length; i += 16) {
        uint8_t block[16];
        // CBC异或
        for (int j = 0; j < 16; j++) {
            block[j] = input[i + j] ^ current_iv[j];
        }
        // SM4加密
        sm4_encrypt_block(rk, block, &output[i]);
        // 更新IV
        memcpy(current_iv, &output[i], 16);
    }
}

