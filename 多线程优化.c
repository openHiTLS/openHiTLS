// 线性变换L
uint32_t L(uint32_t b) {
    return b ^ (b << 2 | b >> (32 - 2)) ^ (b << 10 | b >> (32 - 10)) ^
           (b << 18 | b >> (32 - 18)) ^ (b << 24 | b >> (32 - 24));
}

