// 非线性变换T
uint32_t T(uint32_t x) {
    uint8_t a[4] = {
        (x >> 24) & 0xFF,
        (x >> 16) & 0xFF,
        (x >> 8) & 0xFF,
        x & 0xFF
    };
    for (int i = 0; i < 4; i++)
        a[i] = Sbox[a[i]];
    uint32_t t = ((uint32_t)a[0] << 24) | ((uint32_t)a[1] << 16) |
                 ((uint32_t)a[2] << 8) | a[3];
    return L(t);
}

// 密钥扩展
void key_expansion(uint8_t key[16], uint32_t rk[32]) {
    uint32_t K[36];
    for (int i = 0; i < 4; i++) {
        K[i] = ((uint32_t)key[4 * i] << 24) |
               ((uint32_t)key[4 * i + 1] << 16) |
               ((uint32_t)key[4 * i + 2] << 8) |
               (uint32_t)key[4 * i + 3];
        K[i] ^= FK[i];
    }
    for (int i = 0; i < 32; i++) {
        K[i + 4] = K[i] ^ T(K[i + 1] ^ K[i + 2] ^ K[i + 3] ^ CK[i]);
        rk[i] = K[i + 4];
    }
}

// 单个块加密
void sm4_encrypt_block(uint8_t in[16], uint8_t out[16], uint32_t rk[32]) {
    uint32_t X[36];
    for (int i = 0; i < 4; i++) {
        X[i] = ((uint32_t)in[4 * i] << 24) |
               ((uint32_t)in[4 * i + 1] << 16) |
               ((uint32_t)in[4 * i + 2] << 8) |
               (uint32_t)in[4 * i + 3];
    }
    for (int i = 0; i < 32; i++) {
        X[i + 4] = X[i] ^ T(X[i + 1] ^ X[i + 2] ^ X[i + 3] ^ rk[i]);
    }
    for (int i = 0; i < 4; i++) {
        uint32_t t = X[35 - i];
        out[4 * i]     = (t >> 24) & 0xFF;
        out[4 * i + 1] = (t >> 16) & 0xFF;
        out[4 * i + 2] = (t >> 8) & 0xFF;
        out[4 * i + 3] = t & 0xFF;
    }
}

// 多线程结构
typedef struct {
    int thread_id;
    int thread_num;
    uint8_t *data;
    uint8_t *out;
    uint32_t *rk;
} ThreadArg;

void* thread_func(void* arg) {
    ThreadArg *t = (ThreadArg*)arg;
    int chunk_size = DATA_SIZE / t->thread_num;
    int start = t->thread_id * chunk_size;
    int end = (t->thread_id == t->thread_num - 1) ? DATA_SIZE : start + chunk_size;

    for (int i = start; i < end; i += BLOCK_SIZE) {
        sm4_encrypt_block(t->data + i, t->out + i, t->rk);
    }
    return NULL;
}

// 计时函数
double get_time_ms() {
    struct timespec ts;
    clock_gettime(CLOCK_MONOTONIC, &ts);
    return ts.tv_sec * 1000.0 + ts.tv_nsec / 1e6;
}

int main() {
    uint8_t *data = malloc(DATA_SIZE);
    uint8_t *out = malloc(DATA_SIZE);
    uint32_t rk[32];

    srand(0);
    for (int i = 0; i < DATA_SIZE; i++)
        data[i] = rand() % 256;

    key_expansion(key, rk);

    printf("线程数\t耗时(ms)\t加速比\n");
    double base_time = 0;

    for (int t = 1; t <= MAX_THREADS; t++) {
        pthread_t threads[t];
        ThreadArg args[t];

        double start = get_time_ms();
        for (int i = 0; i < t; i++) {
            args[i].thread_id = i;
            args[i].thread_num = t;
            args[i].data = data;
            args[i].out = out;
            args[i].rk = rk;
            pthread_create(&threads[i], NULL, thread_func, &args[i]);
        }
        for (int i = 0; i < t; i++) {
            pthread_join(threads[i], NULL);
        }
        double end = get_time_ms();
        double elapsed = end - start;

        if (t == 1) base_time = elapsed;
        printf("%d\t%.2f\t\t%.2f\n", t, elapsed, base_time / elapsed);
    }

    free(data);
    free(out);
    return 0;
}
