    uint8_t decrypted[32];

    uint32_t rk[ROUNDS];
    sm4_key_schedule(key, rk);


    uint64_t start_cycles = get_cycles();

    sm4_cbc_encrypt(rk, iv, plaintext, 32, ciphertext);



