// 输出加密结果的函数
void print_cipher(uint32_t cipher[8][4], const char* version_name) {
    printf("%s Encrypted results:\n", version_name);
    for (int i = 0; i < 8; i++) {
        printf("Block %d: ", i);
        for (int j = 0; j < 4; j++) {
            printf("%08x ", cipher[i][j]);
        }
        printf("\n");
    }
}

// 输出表格的格式
void print_table(double original_times[], double optimized_times[], int cache_sizes[], int block_sizes[], int num_cases) {
    printf("\n===========================================\n");
    printf("| Cache Size | Block Size | Original Time | Optimized Time |\n");
    printf("===========================================\n");

    for (int i = 0; i < num_cases; i++) {
        printf("| %-11d | %-10d | %-13.6f | %-14.6f |\n", cache_sizes[i], block_sizes[i], original_times[i], optimized_times[i]);
    }

    printf("===========================================\n");
}

