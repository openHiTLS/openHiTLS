void RVV_ENC_ROUND_FUNCTION_NumBlocks(uint32_t* x, const uint32_t *rk, uint32_t num_blocks, const uint8_t* Sx);
void RVV_DEC_ROUND_FUNCTION_NumBlocks(uint32_t* x, const uint32_t *rk, uint32_t num_blocks, const uint8_t* Sx);

static void RVV_SM4_Crypt_NumBlocks(uint8_t *out, const uint8_t *in, const uint32_t *rk, uint32_t* x, bool enc, uint32_t num_blocks)
{

    for (uint32_t i = 0;i < 4 * num_blocks; i++){
        x[i] = GET_UINT32_BE( in, 4 * i);
    }

    /* Round function */
    if (enc) {
        RVV_ENC_ROUND_FUNCTION_NumBlocks(x, rk, num_blocks, Sbox);  // Encryption
    } else {
        RVV_DEC_ROUND_FUNCTION_NumBlocks(x, rk, num_blocks, Sbox);  // Decryption
    }
    
    
    /* Reverse R(X32 X33 X34 X35) = (X35 X34 X33 X32) */
    
    for (uint32_t i = 0;i < num_blocks;i++){
        PUT_UINT32_BE(x[ 4 * i + 3], out + 16 * i, 0);  // x[3] put into the 4 bytes starting from index 0 of the out
        PUT_UINT32_BE(x[ 4 * i + 2], out + 16 * i, 4);  // x[2] put into the 4 bytes starting from index 4 of the out
        PUT_UINT32_BE(x[ 4 * i + 1], out + 16 * i, 8);  // x[1] put into the 4 bytes starting from index 8 of the out
        PUT_UINT32_BE(x[ 4 * i + 0], out + 16 * i, 12); // x[0] put into the 4 bytes starting from index 12 of the out
    }
}

static int32_t CRYPT_SM4_Crypt(CRYPT_SM4_Ctx *ctx, const uint8_t *in, uint8_t *out, uint32_t length, bool enc)
{
    if (ctx == NULL || in == NULL || out == NULL || length == 0) {
        BSL_ERR_PUSH_ERROR(CRYPT_NULL_INPUT);
        return CRYPT_NULL_INPUT;
    }

    if (length % CRYPT_SM4_BLOCKSIZE != 0) {
        BSL_ERR_PUSH_ERROR(CRYPT_SM4_DATALEN_ERROR);
        return CRYPT_SM4_DATALEN_ERROR;
    }
    int32_t vlenb;
    asm volatile ("csrr %0, vlenb" : "=r" (vlenb));
    int32_t RVV_CRYPT_SM4_BLOCKSIZE = vlenb * 4;
    uint32_t x[32];          // Used as a temporary variable.
    uint32_t blocks = length / CRYPT_SM4_BLOCKSIZE;
    uint32_t num_blocks = vlenb >> 2;
    uint32_t RVV_blocks = blocks / num_blocks;
    uint32_t final_blocks = blocks & (num_blocks - 1);
    uint32_t i = 0;
    for (i = 0; i < RVV_blocks; i++) {
        RVV_SM4_Crypt_NumBlocks(out + i * RVV_CRYPT_SM4_BLOCKSIZE, in + i * RVV_CRYPT_SM4_BLOCKSIZE, ctx->rk, x, enc, num_blocks);
    }
    const uint8_t * tmp_in = in + i * RVV_CRYPT_SM4_BLOCKSIZE;
    uint8_t * tmp_out = out + i * RVV_CRYPT_SM4_BLOCKSIZE;
    RVV_SM4_Crypt_NumBlocks(tmp_out , tmp_in , ctx->rk, x, enc, final_blocks);
    
    if (!enc) {
        (void)memset_s(x, sizeof(x), 0, sizeof(x));
    }
    
    return CRYPT_SUCCESS;
}

int32_t CRYPT_SM4_Encrypt(CRYPT_SM4_Ctx *ctx, const uint8_t *in, uint8_t *out, uint32_t length)
{
    return CRYPT_SM4_Crypt(ctx, in, out, length, true);
}

int32_t CRYPT_SM4_Decrypt(CRYPT_SM4_Ctx *ctx, const uint8_t *in, uint8_t *out, uint32_t length)
{
    return CRYPT_SM4_Crypt(ctx, in, out, length, false);
}

void CRYPT_SM4_Clean(CRYPT_SM4_Ctx *ctx)
{
    if (ctx == NULL) {
        return;
    }
    BSL_SAL_CleanseData((void *)(ctx), sizeof(CRYPT_SM4_Ctx));
    return;
}

