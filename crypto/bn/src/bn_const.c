static BN_BigNum *GetBnConst(BN_BigNum *outConst, BN_BigNum *inConst)
{
    if (outConst == NULL) {
        return BN_Dup(inConst);
    } else {
        if (BN_Copy(outConst, inConst) != CRYPT_SUCCESS) {
            return NULL;
        }
        return outConst;
    }
}

