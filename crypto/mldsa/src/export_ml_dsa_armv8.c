void MLDSA_UseHint(const CRYPT_ML_DSA_Ctx *ctx, int32_t *const h[MLDSA_K_MAX], int32_t *w[MLDSA_K_MAX])
{
    void (*fp)(int32_t *, int32_t const *);
    fp = ctx->info->k == K_VALUE_OF_MLDSA_44? Usehint88: Usehint32;

    for (uint8_t i = 0; i < ctx->info->k; i++) {
        fp(w[i], h[i]);
    }
}

void MLDSA_Decompose(const CRYPT_ML_DSA_Ctx *ctx, int32_t r, int32_t *r1, int32_t *r0)
{
    int32_t t = (int32_t)(((uint32_t)r + 0x7f) >> 7u);
    if (ctx->info->k == K_VALUE_OF_MLDSA_44) {  // If is MLDSA44
        // This is Barrett Modular Multiplication, mod is 22.
        t = (t * 11275u + (1 << 23u)) >> 24u;
        t ^= ((43 - t) >> 31u) & t; // t > 43 ? 0 : t;
    } else {
        t = (t * 1025u + (1 << 21u)) >> 22u;
        t &= 0x0f;
    }

    *r0 = r - t * 2 * ctx->info->gamma2;  // r1  (r+  r0)/(22)
    *r0 -= (((MLDSA_Q - 1) / 2 - *r0) >> 31u) & MLDSA_Q;
    *r1 = t;  // high bits.
}

void MLDSA_Batch_Decompose(const CRYPT_ML_DSA_Ctx *ctx, int32_t a[MLDSA_N], int32_t r1[MLDSA_N])
{
    void (*fp)(int32_t *, int32_t *);
    fp = ctx->info->k == K_VALUE_OF_MLDSA_44? BatchDecompose88: BatchDecompose32;
    fp(a, r1);
}

int32_t MLDSA_RejNTTPoly(int32_t a[MLDSA_N],
    const uint8_t seed[MLDSA_SEED_EXTEND_BYTES_LEN])
{
    int32_t ret = CRYPT_SUCCESS;
    unsigned int outlen = 5 * CRYPT_SHAKE128_BLOCKSIZE;
    uint32_t buflen = 5 * CRYPT_SHAKE128_BLOCKSIZE / 4;
    uint32_t buf[5 * CRYPT_SHAKE128_BLOCKSIZE / 4];

    MldsaShakeCtx ctx;
    MldsaShake128_Init(&ctx);
    MldsaShake128_Absorb(&ctx, seed, MLDSA_SEED_EXTEND_BYTES_LEN);
    MldsaShake128_SqueezeBlocks(&ctx, (uint8_t *)buf, 5);

    uint32_t gensize = MldRejUniformAsm(a, (uint8_t *)buf, outlen, MLD_REJ_UNIFORM_TABLE);
    outlen = CRYPT_SHAKE128_BLOCKSIZE;
    buflen = CRYPT_SHAKE128_BLOCKSIZE / 4;

    for (uint32_t i = gensize, j = buflen + 1; i < MLDSA_N;) {
        if (j >= buflen && i < MLDSA_N) {
            MldsaShake128_SqueezeBlocks(&ctx, (uint8_t *)buf, 1);
            j = 0;
        }

        const uint32_t w0 = CRYPT_HTOLE32(buf[j]);
        const uint32_t w1 = CRYPT_HTOLE32(buf[j + 1]);
        const uint32_t w2 = CRYPT_HTOLE32(buf[j + 2]);

        int32_t t0 = w0;
        int32_t t1 = (w0 >> 24) | (w1 << 8);
        int32_t t2 = (w1 >> 16) | (w2 << 16);
        int32_t t3 = (w2 >> 8);

        t0 &= 0x7FFFFFU;
        t1 &= 0x7FFFFFU;
        t2 &= 0x7FFFFFU;
        t3 &= 0x7FFFFFU;

        const int32_t m0 = (MLDSA_Q - 1 - t0) >> 31;
        const int32_t m1 = (MLDSA_Q - 1 - t1) >> 31;
        const int32_t m2 = (MLDSA_Q - 1 - t2) >> 31;
        const int32_t m3 = (MLDSA_Q - 1 - t3) >> 31;

        a[i] = t0 & ~m0;
        i += 1 + m0;
        if (i < MLDSA_N) {
            a[i] = t1 & ~m1;
            i += 1 + m1;
        }

        if (i < MLDSA_N) {
            a[i] = t2 & ~m2;
            i += 1 + m2;
        }

        if (i < MLDSA_N) {
            a[i] = t3 & ~m3;
            i += 1 + m3;
        }

        j += 3;
    }

    return ret;
}

int32_t MLDSA_RejBoundedPolyEta2(int32_t *a, const uint8_t *s)
{
    (void) s;
    uint8_t buf[CRYPT_SHAKE256_BLOCKSIZE] = {0};
    uint32_t bufLen = CRYPT_SHAKE256_BLOCKSIZE;
    int32_t ret = CRYPT_SUCCESS;
    uint32_t gensize = 0;

    MldsaShakeCtx ctx;
    MldsaShake256_Init(&ctx);
    MldsaShake256_Absorb(&ctx, s, MLDSA_PRIVATE_SEED_LEN + 2);
    MldsaShake256_SqueezeBlocks(&ctx, buf, 1);

    gensize = MldRejUniformEta2Asm(a, buf, bufLen, MLD_REJ_UNIFORM_ETA_TABLE);

    for (uint32_t i = gensize, j = bufLen + 1; i < MLDSA_N; j++) {
        if (j >= CRYPT_SHAKE256_BLOCKSIZE) {
            MldsaShake256_SqueezeBlocks(&ctx, buf, 1);
            j = 0;
        }
        int32_t z0 = (int32_t)(buf[j] & 0x0F);
        int32_t z1 = (int32_t)(buf[j] >> 4u);

        int32_t mask = (0xE - z0) >> 31;
        z0 = z0 - ((205 * z0) >> 10) * 5;
        a[i] = (2 - z0) & ~mask;
        i += 1 + mask;

        if (i < MLDSA_N) {
            mask = (0xE - z1) >> 31;
            z1 = z1 - ((205 * z1) >> 10) * 5;
            a[i] = (2 - z1) & ~mask;
            i += 1 + mask;
        }
    }

    return ret;
}

int32_t MLDSA_RejBoundedPolyEta4(int32_t *a, const uint8_t *s)
{
    (void) s;
    uint8_t buf[2*CRYPT_SHAKE256_BLOCKSIZE] = {0};
    uint32_t bufLen = 2*CRYPT_SHAKE256_BLOCKSIZE;
    int32_t ret = CRYPT_SUCCESS;
    uint32_t gensize = 0;

    MldsaShakeCtx ctx;
    MldsaShake256_Init(&ctx);
    MldsaShake256_Absorb(&ctx, s, MLDSA_PRIVATE_SEED_LEN + 2);
    MldsaShake256_SqueezeBlocks(&ctx, buf, 2);

    gensize = MldRejUniformEta4Asm(a, buf, bufLen, MLD_REJ_UNIFORM_ETA_TABLE);

    for (uint32_t i = gensize, j = bufLen + 1; i < MLDSA_N; j++) {
        if (j >= CRYPT_SHAKE256_BLOCKSIZE) {
            MldsaShake256_SqueezeBlocks(&ctx, buf, 1);
            j = 0;
        }
        int32_t z0 = (int32_t)(buf[j] & 0x0F);
        int32_t z1 = (int32_t)(buf[j] >> 4u);
        int32_t mask = (0x8 - z0) >> 31;
        a[i] = (4 - z0) & ~mask;
        i += 1 + mask;

        if (i < MLDSA_N) {
            mask = (0x8 - z1) >> 31;
            a[i] = (4 - z1) & ~mask;
            i += 1 + mask;
        }
    }

    return ret;
}

