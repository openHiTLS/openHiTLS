/*
 * MLDSA_RejNTTPolyPair - fill two NTT polynomials via SHAKE128 x2.
 *
 * Both seeds share the 32-byte public seed prefix; they differ only in the
 * last two bytes encoding the matrix position. The dual-lane Keccak
 * permutation processes both streams simultaneously.
 *
 * Initial squeeze: 5 SHAKE128 blocks (840 bytes, ~280 candidates per lane)
 * covers the required 256 coefficients in >99.9% of calls.
 */
int32_t MLDSA_RejNTTPolyPair(int32_t *a0, int32_t *a1,
    const uint8_t *seed0, const uint8_t *seed1)
{
    const uint32_t INIT_BLOCKS = 5;
    const uint32_t INIT_LEN    = INIT_BLOCKS * CRYPT_SHAKE128_BLOCKSIZE;

    uint8_t buf0[5 * CRYPT_SHAKE128_BLOCKSIZE];
    uint8_t buf1[5 * CRYPT_SHAKE128_BLOCKSIZE];
    Keccakx2State state;

    Keccakx2Absorb(state, CRYPT_SHAKE128_BLOCKSIZE,
                   seed0, seed1, MLDSA_SEED_EXTEND_BYTES_LEN, 0x1F);
    Keccakx2Squeeze(buf0, buf1, INIT_BLOCKS, CRYPT_SHAKE128_BLOCKSIZE, state);

    /* The ASM rejection kernel expects little-endian uint32_t words. */
    for (uint32_t k = 0; k < INIT_LEN / 4; k++) {
        ((uint32_t *)buf0)[k] = CRYPT_HTOLE32(((uint32_t *)buf0)[k]);
        ((uint32_t *)buf1)[k] = CRYPT_HTOLE32(((uint32_t *)buf1)[k]);
    }

    uint32_t i0 = (uint32_t)MldRejUniformAsm(a0, buf0, INIT_LEN, MLD_REJ_UNIFORM_TABLE);
    uint32_t i1 = (uint32_t)MldRejUniformAsm(a1, buf1, INIT_LEN, MLD_REJ_UNIFORM_TABLE);

    /* Fallback: squeeze one block at a time for both lanes until both done. */
    uint8_t fb0[CRYPT_SHAKE128_BLOCKSIZE];
    uint8_t fb1[CRYPT_SHAKE128_BLOCKSIZE];

    while (i0 < MLDSA_N || i1 < MLDSA_N) {
        Keccakx2Squeeze(fb0, fb1, 1, CRYPT_SHAKE128_BLOCKSIZE, state);

        const uint32_t *f0 = (const uint32_t *)fb0;
        const uint32_t *f1 = (const uint32_t *)fb1;
        for (uint32_t j = 0; j + 2 < CRYPT_SHAKE128_BLOCKSIZE / 4; j += 3) {
            uint32_t w00 = CRYPT_HTOLE32(f0[j]);
            uint32_t w01 = CRYPT_HTOLE32(f0[j + 1]);
            uint32_t w02 = CRYPT_HTOLE32(f0[j + 2]);
            uint32_t w10 = CRYPT_HTOLE32(f1[j]);
            uint32_t w11 = CRYPT_HTOLE32(f1[j + 1]);
            uint32_t w12 = CRYPT_HTOLE32(f1[j + 2]);

            int32_t t[4][2];
            t[0][0] = (int32_t)( w00                       & 0x7FFFFFU);
            t[1][0] = (int32_t)(((w00 >> 24) | (w01 << 8)) & 0x7FFFFFU);
            t[2][0] = (int32_t)(((w01 >> 16) | (w02 << 16))& 0x7FFFFFU);
            t[3][0] = (int32_t)( (w02 >> 8)                & 0x7FFFFFU);
            t[0][1] = (int32_t)( w10                       & 0x7FFFFFU);
            t[1][1] = (int32_t)(((w10 >> 24) | (w11 << 8)) & 0x7FFFFFU);
            t[2][1] = (int32_t)(((w11 >> 16) | (w12 << 16))& 0x7FFFFFU);
            t[3][1] = (int32_t)( (w12 >> 8)                & 0x7FFFFFU);

            for (uint32_t n = 0; n < 4; n++) {
                int32_t m = (MLDSA_Q - 1 - t[n][0]) >> 31;
                if (i0 < MLDSA_N) { a0[i0] = t[n][0] & ~m; i0 += 1 + (uint32_t)m; }
                m = (MLDSA_Q - 1 - t[n][1]) >> 31;
                if (i1 < MLDSA_N) { a1[i1] = t[n][1] & ~m; i1 += 1 + (uint32_t)m; }
            }
        }
    }
    return CRYPT_SUCCESS;
}

/*
 * MLDSA_RejBoundedPolyEta2Pair - fill two eta=2 polynomials via SHAKE256 x2.
 *
 * Acceptance rate 15/16 per nibble → ~273 bytes needed per polynomial.
 * 2 blocks × 136 bytes = 272 bytes covers this in almost all cases.
 */
int32_t MLDSA_RejBoundedPolyEta2Pair(int32_t *a0, int32_t *a1,
    const uint8_t *s0, const uint8_t *s1)
{
    const uint32_t INIT_BLOCKS = 2;
    const uint32_t INIT_LEN    = INIT_BLOCKS * CRYPT_SHAKE256_BLOCKSIZE;

    uint8_t buf0[2 * CRYPT_SHAKE256_BLOCKSIZE];
    uint8_t buf1[2 * CRYPT_SHAKE256_BLOCKSIZE];
    Keccakx2State state;

    Keccakx2Absorb(state, CRYPT_SHAKE256_BLOCKSIZE,
                   s0, s1, MLDSA_PRIVATE_SEED_LEN + 2, 0x1F);
    Keccakx2Squeeze(buf0, buf1, INIT_BLOCKS, CRYPT_SHAKE256_BLOCKSIZE, state);

    uint32_t i0 = (uint32_t)MldRejUniformEta2Asm(a0, buf0, INIT_LEN, MLD_REJ_UNIFORM_ETA_TABLE);
    uint32_t i1 = (uint32_t)MldRejUniformEta2Asm(a1, buf1, INIT_LEN, MLD_REJ_UNIFORM_ETA_TABLE);

    uint8_t fb0[CRYPT_SHAKE256_BLOCKSIZE];
    uint8_t fb1[CRYPT_SHAKE256_BLOCKSIZE];

    while (i0 < MLDSA_N || i1 < MLDSA_N) {
        Keccakx2Squeeze(fb0, fb1, 1, CRYPT_SHAKE256_BLOCKSIZE, state);

        for (uint32_t j = 0; j < CRYPT_SHAKE256_BLOCKSIZE; j++) {
            if (i0 < MLDSA_N) {
                int32_t z0   = (int32_t)(fb0[j] & 0x0F);
                int32_t mask = (0xE - z0) >> 31;
                z0 = z0 - ((205 * z0) >> 10) * 5;
                a0[i0] = (2 - z0) & ~mask;
                i0 += (uint32_t)(1 + mask);
            }
            if (i0 < MLDSA_N) {
                int32_t z1   = (int32_t)(fb0[j] >> 4);
                int32_t mask = (0xE - z1) >> 31;
                z1 = z1 - ((205 * z1) >> 10) * 5;
                a0[i0] = (2 - z1) & ~mask;
                i0 += (uint32_t)(1 + mask);
            }
            if (i1 < MLDSA_N) {
                int32_t z0   = (int32_t)(fb1[j] & 0x0F);
                int32_t mask = (0xE - z0) >> 31;
                z0 = z0 - ((205 * z0) >> 10) * 5;
                a1[i1] = (2 - z0) & ~mask;
                i1 += (uint32_t)(1 + mask);
            }
            if (i1 < MLDSA_N) {
                int32_t z1   = (int32_t)(fb1[j] >> 4);
                int32_t mask = (0xE - z1) >> 31;
                z1 = z1 - ((205 * z1) >> 10) * 5;
                a1[i1] = (2 - z1) & ~mask;
                i1 += (uint32_t)(1 + mask);
            }
        }
    }
    return CRYPT_SUCCESS;
}

