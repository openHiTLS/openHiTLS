/*
 * ML-DSA polynomial arithmetic backend, ARMv8 NEON implementation.
 *
 * This file provides the same four entry points as the portable C backend in
 * ml_dsa_ntt.c (MLDSA_ComputesNTT, MLDSA_ComputesINVNTT, MLDSA_VectorsMul,
 * MLDSA_MatrixMulAcc), backed by the assembly in asm/ml_dsa_ntt_armv8.S.
 *
 * Arithmetic style (Becker/Hwang/Kannwischer/Yang/Yang, "Neon NTT",
 * https://eprint.iacr.org/2021/986):
 *   Each NTT twiddle z is stored as a pair (zl, zh):
 *     zl = z mod+- q                       (signed canonical representative)
 *     zh = round(zl * 2^31 / q)            (Barrett constant for sqrdmulh)
 *   and a Barrett modular multiplication of a coefficient a by z is
 *     sqrdmulh hi, a, zh    // hi  = round(a * zh / 2^31) ~= round(a * z / q)
 *     mul      lo, a, zl    // lo  = a * z mod 2^32
 *     mls      lo, hi, q    // lo  = a * z - hi * q  ==  a * z mod+- q
 *   which is exact mod q with |result| <= q * (1/2 + |a| / 2^32).
 *
 * Value-range/domain contract (differs from the C backend internally; the
 * external behaviour of the four entry points composed as used by
 * ml_dsa_core.c is identical mod q):
 *   - MLDSA_ComputesNTT: input in (-q, q), output exact NTT values, |out| < 5q.
 *   - MLDSA_VectorsMul / MLDSA_MatrixMulAcc: signed Montgomery reduction of
 *     the 64-bit (accumulated) products followed by a Barrett reduction;
 *     output == (sum a_i * b_i) * 2^-32 mod q with |out| <= q/2 + 1.
 *   - MLDSA_ComputesINVNTT: Gentleman-Sande inverse NTT whose final layer is
 *     fused with multiplication by F = 256^-1 * 2^32 == 2^24 mod q, cancelling
 *     both the 1/256 inverse-transform scaling and the single 2^-32 Montgomery
 *     factor introduced by the pointwise multiplication; |out| <= 3q/4.
 *
 * Twiddle table layout (consumption order of the assembly):
 *   MLDSA_NTT_TWIDDLE_TABLE, 544 x int32:
 *     [0..31]   NTT top (layers len=128/64/32/16): (zl, zh) for zeta_1..zeta_15,
 *               then {q, 0} padding (q is read from lane v7.s[2]).
 *     [32..543] NTT bottom: 8 iterations x 64 entries covering two 16-coefficient
 *               blocks B0 = 2i, B1 = 2i+1:
 *                 {z_{16+B0}, z_{16+B1}}                       (len=8 scalars)
 *                 {z_{32+2B0}, z_{33+2B0}}                     (len=4, B0)
 *                 {z_{32+2B1}, z_{33+2B1}}                     (len=4, B1)
 *                 {0, 0, 0, 0}                                 (padding)
 *               then per block B in {B0, B1}:
 *                 Z2lo/Z2hi   = lanes z_{64+4B+v},   v = 0..3  (len=2, transposed)
 *                 Zelo/Zehi   = lanes z_{128+8B+2v}, v = 0..3  (len=1, even pairs)
 *                 Zolo/Zohi   = lanes z_{128+8B+2v+1}          (len=1, odd pairs)
 *   MLDSA_INTT_TWIDDLE_TABLE, 544 x int32: mirrored structure, bottom first.
 *     Inverse twiddles follow the C INVNTT convention (-zeta_m with m walking
 *     downward); the final fused layer uses F = 2^24 mod q and
 *     G = F * (-zeta_1) mod+- q (note -zeta_1 == zeta_1^-1 since zeta_1^2 == -1).
 *
 * Generation script (verified against ml_dsa_ntt.c ZETAS and a schoolbook
 * negacyclic product; see check comments inline):
 *
 *   Q = 8380417; N = 256; ROOT = 1753
 *   def bitrev8(x):
 *       r = 0
 *       for _ in range(8): r = (r << 1) | (x & 1); x >>= 1
 *       return r
 *   ZETA = [pow(ROOT, bitrev8(m), Q) for m in range(N)]
 *   def center(x, mod):
 *       x %= mod
 *       return x - mod if x > mod // 2 else x
 *   def pair(z):
 *       zl = center(z, Q)
 *       return zl, (2 * zl * (1 << 31) + Q) // (2 * Q)   # round, q odd: no ties
 *   def fwd_pairs(m): return pair(ZETA[m])
 *   def inv_pairs(m): return pair(Q - ZETA[m])
 *   F = (2**32 * pow(256, -1, Q)) % Q                    # == 2^24 mod q == 16382
 *   def ntt_table():
 *       T = []
 *       for m in range(1, 16): T += fwd_pairs(m)
 *       T += [Q, 0]
 *       for it in range(8):
 *           B0, B1 = 2 * it, 2 * it + 1
 *           T += [*fwd_pairs(16 + B0), *fwd_pairs(16 + B1)]
 *           T += [*fwd_pairs(32 + 2 * B0), *fwd_pairs(33 + 2 * B0)]
 *           T += [*fwd_pairs(32 + 2 * B1), *fwd_pairs(33 + 2 * B1)]
 *           T += [0, 0, 0, 0]
 *           for B in (B0, B1):
 *               z2 = [fwd_pairs(64 + 4 * B + v) for v in range(4)]
 *               ze = [fwd_pairs(128 + 8 * B + 2 * v) for v in range(4)]
 *               zo = [fwd_pairs(128 + 8 * B + 2 * v + 1) for v in range(4)]
 *               for grp in (z2, ze, zo):
 *                   T += [p[0] for p in grp] + [p[1] for p in grp]
 *       return T
 *   def intt_table():
 *       T = []
 *       for it in range(8):
 *           B0, B1 = 2 * it, 2 * it + 1
 *           T += [*inv_pairs(31 - B0), *inv_pairs(31 - B1)]
 *           T += [*inv_pairs(63 - 2 * B0), *inv_pairs(62 - 2 * B0)]
 *           T += [*inv_pairs(63 - 2 * B1), *inv_pairs(62 - 2 * B1)]
 *           T += [0, 0, 0, 0]
 *           for B in (B0, B1):
 *               ze = [inv_pairs(255 - 8 * B - 2 * v) for v in range(4)]
 *               zo = [inv_pairs(254 - 8 * B - 2 * v) for v in range(4)]
 *               z2 = [inv_pairs(127 - 4 * B - v) for v in range(4)]
 *               for grp in (ze, zo, z2):
 *                   T += [p[0] for p in grp] + [p[1] for p in grp]
 *       for u in range(8): T += inv_pairs(15 - u)
 *       for u in range(4): T += inv_pairs(7 - u)
 *       T += inv_pairs(3) + inv_pairs(2)
 *       T += pair(F) + pair((F * (Q - ZETA[1])) % Q)
 *       return T
 */

